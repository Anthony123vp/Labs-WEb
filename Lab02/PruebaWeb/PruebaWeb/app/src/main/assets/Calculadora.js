class Calculadora {
    sumar(num1, num2) {
        var result = num1 + num2;
        return result;
    }

    restar(num1, num2) {
        var result = num1 - num2;
        return result;
    }

    dividir(num1, num2) {
        var result = num1 / num2;
        return result;
    }

    multiplicar(num1, num2) {
        var result = num1 * num2;
        return result;
    }
} 